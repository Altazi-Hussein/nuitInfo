     */
    public function store(Request $request)
    {
        $article = new Article;
        $article->title = $request->input('title');
        $article->content = $request->input('content');
        $article->user_id = Auth::id();
        $article->save();

        return redirect()->back()->with('success', 'Article ajoutÃ© !');
    }

